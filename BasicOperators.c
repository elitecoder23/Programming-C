#include <stdio.h>

int main() {

    int a = 24;
    int b = 4;

    // Addition
    int sum = a + b;
    printf("The sum is %d", sum);

    //Subtraction
    int sub = a - b;
    printf("The subtraction result is: %d", sub);

    // Multiplication
    int multiply = a * b;
    printf("The multiplying result is: %d", multiply);

    // Division
    int divide = a / b;
    printf("The dividing result is: %d", divide);

    // Modulus
    int mod = a % b;
    printf("The modulus result is: %d", mod);

    return 0;




}
    