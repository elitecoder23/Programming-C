// File to practice writing different data types 
#include <stdio.h>


int main(){

    // integer
    int numInteger = 5;

    // float
    float numFloat = 5.0;

    // double
    double numDouble = 5.12345;

    // character
    char letterA = 'A';

    printf("Integer Number: %d\n", numInteger);
    printf("Float number: %.1f\n", numFloat);
    printf("Double number: %.5lf\n", numDouble);
    printf("Grade: %c\n", letterA);
}
